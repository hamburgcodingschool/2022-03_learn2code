// ARRAYS
let myArray = [1, 2, 3]; // Definition

console.log(myArray.length); // Length of the array
console.log(myArray[0]); // First element in the array
console.log(myArray[myArray.length - 1]); // last element of the array

let myNameArray = ["Andi", "Agustina", "Celine"]; // Arrays can contain anything
console.log(myNameArray[myNameArray.length - 1]);

myNameArray[3] = "Jan";
console.log(myNameArray); // ["Andy", "Agustina", "Celine", "Jan"]
